
#7.Modificar el ejercicio 6 para que reciba un entero y un float.
num1=int(input("Ingrese el 1er nro entero: "))
num2=float(input("Ingrese el 2do nro con coma: "))
# a. suma
print(f"{num1}+{num2}={num1+num2}")
# b. resta
print(f"{num1}-{num2}={num1-num2}")
# c. el producto
print(f"{num1}*{num2}={num1*num2}")
# d. la potencia
print(f"{num1}**{num2}={num1**num2}")
# e. el resto
print(f"{num1}%{num2}={num1%num2}")

